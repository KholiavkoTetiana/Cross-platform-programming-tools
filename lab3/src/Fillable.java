
/**
 * Інтерфейс для об'єктів, які можна "заповнювати" (заливати воду/паливо і т.д.).
 */
public interface Fillable {
    /**
     * Додати одиниць (наприклад, мілілітрів) у резервуар/магазин.
     * @param amount кількість одиниць (позитивне число)
     * @return фактично додано (може бути менше, якщо не вистачило місця)
     */
    int fill(int amount);

    /**
     * Повертає поточний рівень (в одиницях).
     * @return рівень
     */
    int currentLevel();
}
